export const locale = writable('en');

export const t = derived(locale, ($locale) => {
  return (key, defaultValue) => {
    const entry = langs[key];
    if (entry) {
      if (entry[$locale] !== undefined && entry[$locale] !== '') {
        return entry[$locale];
      }
      // Fallback to English
      if (entry['en'] !== undefined && entry['en'] !== '') {
        return entry['en'];
      }
    }
    return defaultValue;
  };
});

